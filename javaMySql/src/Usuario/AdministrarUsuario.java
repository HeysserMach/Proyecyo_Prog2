    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int fila = usuario_tabla.getSelectedRow();
        if (fila >= 0) {
            txtName.setText(usuario_tabla.getValueAt(fila, 0).toString());
            txtPass.setText(usuario_tabla.getValueAt(fila, 1).toString());
            txtAlumno.setText(usuario_tabla.getValueAt(fila, 2).toString());
        } else {
            JOptionPane.showMessageDialog(null, "Fila no seleccionada");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = usuario_tabla.getSelectedRow();
        String Valor = usuario_tabla.getValueAt(fila, 0).toString();
        if (fila >= 0) {
            try {
                Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/unah_prog2", "root", "heysser022");
                PreparedStatement pps = connection.prepareStatement("DELETE from usuario where name='" + Valor + "'");
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Dato Eliminado");
                MostrarTabla();
            } catch (SQLException e) {

                JOptionPane.showMessageDialog(null, "Error" + e.getMessage());

            }

    }//GEN-LAST:event_btnEliminarActionPerformed
    }
