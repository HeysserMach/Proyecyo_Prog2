    private void Actualizar() {
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/unah_prog2", "root", "heysser022");
            PreparedStatement pps = connection.prepareStatement("UPDATE usuario SET password='" + txtPass.getText() + "', alumno='" + txtAlumno.getText() + "'"
                    + "Where name= '" + txtName.getText() + "'");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos Modificados");
            MostrarTabla();
        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());

        }

    }

    public void MostrarTabla() {
        DefaultTableModel Mostrar = new DefaultTableModel();
        Mostrar.addColumn("Name");
        Mostrar.addColumn("Password");
        Mostrar.addColumn("Alumno");

        usuario_tabla.setModel(Mostrar);

        String[] datos = new String[3];

        try {
            Statement leer = con.createStatement();
            ResultSet rs = leer.executeQuery("SELECT * FROM usuario");

            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                Mostrar.addRow(datos);

            }
            usuario_tabla.setModel(Mostrar);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
        }
    }

    private void Limpiar() {
        txtName.setText(null);
        txtPass.setText(null);
        txtAlumno.setText(null);
    }

